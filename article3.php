<h1 class="entry-title">Gabriel Iglesias Breaks Even at the Ted</h1>
<time class="entry-date" datetime="2016-04-18T16:12:42+00:00">April 18, 2016</time>
<p> <b>George Plank</b> </p> 
<p> <b>Staff Writer</b> </p> 
<p>Someone passing by the Ted Constant Convocation Center on Saturday night, April 9, might have wondered what was so funny with the uproarious laughter coming from within. Those on the inside enjoyed comedy from the talented Gabriel Iglesias who tours the country through the fall promoting his new show, “Fluffy Breaks Even.”</p>
<p>Prior to taking the stage, Iglesias invited his co-stars to warm up the audience. Martin Moreno, Alfred Robles and G Reilly made it clear no subject matter was off limits that evening. Jokes about marijuana, Michael Jackson and Bill Cosby followed.</p>
<p>After a brief intermission, the curtains were pulled back to reveal Iglesias. The audience instantly knew they were in for a great time.</p>

<img src="26074277160_437ea73e15_o-1.jpg">

<p>“It’s a pretty good show,” Iglesias insisted. “I’ve seen it four times and I know how it ends.”</p>
<p>His set consisted of mostly new material and covered every topic from Batman and false death reports to the time he met Arnold Schwarzenegger and his potential ABC sitcom. Nearly every moment was infused with his trademark voices and silly sound effects.</p>
<p>Iglesias warned the audience early on the show was going to run long. His set, which was only supposed to last 45 minutes, ended up 47 minutes over his time limit before Iglesias unplugged the timer.</p>
<p>In addition to his new material, Iglesias performed a number of encore jokes from his earlier specials. During each reprise, audience members shouted out the punchline in unison with the comedian.</p>
<p>“When did my jokes become songs?” Iglesias asked. Once each joke was finished, the audience offered other suggestions. “Now you guys are yelling my jokes at me like I’m a comedy mariachi,” Iglesias said.</p>
<p>Among these suggestions was Iglesias’ famed joke about a racist gift basket in which he surprises his black friend with a basket filled with stereotypically “black” gifts. Since the original telling of that joke, Iglesias has received several racist gift baskets of his own.</p>
<p>Iglesias recounted the time he was in Mobile, Alabama, when a fan rushed the stage with one such basket. Inside were such things as gardening supplies, a Home Depot gift card and an application for U.S. citizenship.</p>
<p>The sort of dalliance was nowhere to be seen at the Ted. Iglesias maintained complete control of the audience throughout the evening. At one point, he succeeded in leading the audience in a chant for one of the security guards named Anthony.</p>
<p>Iglesias has seen incredible success as a comedian.</p>
<p>“I started comedy… it will be 19 years at midnight,” Iglesias announced.</p>
<p>According to Iglesias, he started doing comedy on April 10, 1997. He hinted at a potential sitcom on ABC in the near future, but promised no matter the result of the endeavor, he will never stop doing comedy. As evident by their standing ovation, the audience can hardly wait for the next time Fluffy comes to town.</p>
