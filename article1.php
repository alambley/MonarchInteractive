<h1 class="entry-title">Monarch Baseball Wins First of Series 5-2 Over UTSA</h1>
<time class="entry-date" datetime="2016-04-18T16:12:42+00:00">April 18, 2016</time>
<p> <b>Michal High</b> </p> 
<p> <b>Contributing Writer</b> </p> 
<p>The Old Dominion Monarchs (24-10, 7-6) extended their winning streak to five games, including four Conference USA victories, with a 5-2 victory over the University of Texas-San Antonio Roadrunners (12-21, 3-10), Friday at the Bud Metheny Baseball Complex.</p>
<p>The Monarchs scored two runs in the fifth inning with two errors on one play.</p>
<p>Justin Hayes and Nick Walker led off the inning with back-to-back singles. Zach Rutherford laid down a near perfect bunt, causing UTSA pitcher to rush the throw, which ended up in right field. The right fielder mishandled it and when the dust settled, Hayes and Walker both scored safely with Rutherford on third base. He was erased at home as he attempted to score on a fly ball to left field. However, Ryan Stacy fired a strike to the catcher and Mason George applied the tag to end the inning.</p>
<p>The Monarchs’ bullpen shut down the Roadrunners’ offense. They allowed one run on three hits, while allowing two walks and fanning eight in 5.2 innings.</p>
<p>The normal Friday starter, Sam Sinnen, replaced Nick Hartman with one out and one on in the fourth inning. He tossed 3.2 scoreless innings, allowing one hit and one base-on-balls, while striking out four.</p>
<p>UTSA starting pitcher, Aaron Burns was the tough luck loser. He scattered nine hits in seven innings of work. Burns allowed five runs, struck out five and walked one, while tossing 117 pitches.</p>
<p>UTSA jumped out to the 1-0 lead in the first inning. Leadoff man Skylar Valentine doubled and stole third. C.J. Pickering struck out, which would have ended the inning. However, when he advanced to first base on a dropped third strike, Valentine scampered home during the throw.</p>
<p>Old Dominion would answer with a run of their own in the bottom of the first inning.</p>
<p>Nick Walker, who was hit-by-pitch to lead the game, came home on a one out single by Connor Myers. The next two batters were retired, ending the threat.</p>
<p>“We have scored runs in the first inning all season long,” Old Dominion head coach Chris Finwood said.</p>
<p>They would add one in the seventh to extend the lead to three before UTSA registered one of their own in the eighth.</p>
<p>The Monarchs’ Morgan Maguire struck out the two batters he faced in the eighth to stop the rally. He allowed one hit in the ninth before retiring the next three batters to earn his second save of the season.</p>
